import { z } from 'zod';
import type { McpServer } from '@modelcontextprotocol/sdk/server/mcp.js';

export function registerParentNewsletterTool(server: McpServer) {
  server.tool(
    'generate_parent_newsletter',
    '가정통신문 작성을 위한 가이드라인과 프롬프트를 반환합니다. 안내용 또는 회신용 가정통신문을 작성할 수 있으며, 결과물은 export_hwpx의 parent-newsletter 템플릿으로 HWPX 파일로 출력할 수 있습니다.',
    {
      content: z.string().describe('가정통신문 내용 또는 참고 자료 (행사 안내, 설문, 동의서 등)'),
      instructions: z.string().optional().describe('추가 지침 (톤, 강조 사항 등)'),
      school_name: z.string().optional().describe('학교명'),
      newsletter_type: z.enum(['info', 'reply']).default('info').describe('유형: info=안내용(기본), reply=회신용(동의서/설문 포함)'),
      include_reply_slip: z.boolean().default(false).describe('회신란(절취선+서명란) 포함 여부'),
      principal_name: z.string().optional().describe('학교장 이름'),
      contact_info: z.string().optional().describe('문의처 (담당 교사명, 전화번호 등)'),
    },
    async (params) => {
      const schoolName = params.school_name || '○○학교';
      const principalName = params.principal_name || '○○○';
      const hasReply = params.newsletter_type === 'reply' || params.include_reply_slip;

      const guideMarkdown = [
        `# 가정통신문 생성 가이드`,
        ``,
        `## 입력 정보`,
        `- 학교명: ${schoolName}`,
        `- 유형: ${params.newsletter_type === 'reply' ? '회신용 (동의서/설문 포함)' : '안내용'}`,
        `- 회신란: ${hasReply ? '포함' : '미포함'}`,
        `- 학교장: ${principalName}`,
        params.contact_info ? `- 문의처: ${params.contact_info}` : '',
        ``,
        `## 참고 내용`,
        `${params.content}`,
        ``,
        params.instructions ? `## 추가 지침\n${params.instructions}\n` : '',
        `## 가정통신문 작성 규칙`,
        ``,
        `### 기본 구조`,
        `1. **학교명**: 상단 중앙에 학교 이름`,
        `2. **제목**: "- ○○○ 안내 -" 형식 (중앙, 볼드)`,
        `3. **인사말**: 계절/시기에 맞는 인사 (1~2문장)`,
        `   - 봄: "따스한 봄 햇살이 교정을 비추는 계절입니다."`,
        `   - 여름: "초록빛이 짙어지는 계절, 학부모님의 가정에 건강과 행복이 함께하시길 바랍니다."`,
        `   - 가을: "풍성한 가을을 맞아 학부모님 가정에 안녕을 기원합니다."`,
        `   - 겨울: "추운 겨울, 가정에 늘 따뜻한 웃음이 가득하시길 바랍니다."`,
        `4. **본문**: 안내 내용 (행사, 변경사항, 요청사항 등)`,
        `5. **세부안내**: 일시, 장소, 준비물, 유의사항 등 (표 또는 목록)`,
        `6. **문의안내**: "문의: ○○부 ○○○ 선생님 (☎ 02-XXX-XXXX)"`,
        `7. **날짜**: "2026년 5월 14일" 형식`,
        `8. **학교장명**: "${schoolName}장  ${principalName}" (중앙)`,
        ``,
        hasReply ? [
          `### 회신란 구조 (절취선 하단)`,
          `- 절취선: "- - - - - 절 취 선 - - - - -"`,
          `- 회신 제목: "○○○ 회신서" (중앙, 볼드)`,
          `- 회신 내용: 동의 여부 체크란, 선택 항목 등`,
          `  - 동의서: "☐ 동의합니다   ☐ 동의하지 않습니다"`,
          `  - 설문: 선택/서술 항목`,
          `- 서명란: "학년___반___번  학생 이름:_________  보호자 서명:_________"`,
          ``,
        ].join('\n') : '',
        `### 문체 규칙`,
        `- 존댓말 사용: "~합니다", "~바랍니다", "~드립니다"`,
        `- 따뜻하고 공손한 어조`,
        `- 핵심 정보는 볼드 또는 밑줄로 강조`,
        `- 긴 내용은 표로 정리`,
        ``,
        `### 금지 사항`,
        `- 명령조 사용 금지 ("~하시오", "~해야 합니다")`,
        `- 과도한 전문 용어 사용 금지`,
        `- 불필요한 반복 금지`,
        ``,
        `## HWPX 출력 방법`,
        ``,
        `\`export_hwpx\` 도구를 사용하여 HWPX 파일로 출력하세요.`,
        ``,
        `### export_hwpx 파라미터`,
        `- **template**: \`"parent-newsletter"\``,
        `- **section_xml**: 아래 채울 자리 표시에 내용을 채워서 전달`,
        ``,
        `### 채울 자리 표시 매핑`,
        ``,
        `**헤더 영역**`,
        `- {{학교명}}: ${schoolName}`,
        `- {{전화번호}}: 학교 대표 전화번호 (예: "02-1234-5678")`,
        `- {{팩스번호}}: FAX 번호 (예: "02-1234-5679")`,
        `- {{문서번호}}: 가정통신문 번호 (예: "가정통신문 제2026-42호")`,
        ``,
        `**본문 영역**`,
        `- {{제목}}: 가정통신문 제목 (예: "현장체험학습 안내")`,
        `- {{인사말}}: 계절 인사 + 학부모 감사 (1~2문장)`,
        `- {{본문내용}}: 안내 본문 내용`,
        ``,
        `**세부안내 표 (9행×2열)**`,
        `- {{세부안내제목}}: 세부안내 표 상단 제목 (예: "세부 안내 사항")`,
        `- {{세부항목1}} ~ {{세부항목8}}: 좌측 열 항목명 (일시, 장소, 대상, 준비물 등)`,
        `- {{세부내용1}} ~ {{세부내용8}}: 우측 열 내용`,
        ``,
        `**유의사항**`,
        `- {{유의사항제목}}: 유의사항 섹션 제목 (예: "유의사항")`,
        `- {{유의사항1}} ~ {{유의사항4}}: 개별 유의사항 항목`,
        ``,
        `**하단 서명**`,
        `- {{문의안내}}: 문의처 정보 (예: "문의: ○○부 ○○○ 선생님 (☎ 02-1234-5678)")`,
        `- {{날짜}}: 발송일 (예: "2026년 5월 14일")`,
        `- {{발신부서}}: 발신 부서명 (예: "○○초등학교 교무기획부")`,
        `- {{학교장명}}: "${schoolName}장  ${principalName}"`,
        ``,
        `**회신란 (절취선 하단)**`,
        hasReply ? `- {{회신제목}}: 회신서 제목 (예: "현장체험학습 참가 동의서")` : '',
        hasReply ? `- {{회신안내}}: 동의 안내 문구` : '',
        hasReply ? `- {{동의선택}}: 동의 체크 옵션 (예: "☐ 동의합니다  ☐ 동의하지 않습니다")` : '',
        ``,
        `### 주의사항`,
        `- 절취선(✂)과 서명란(학년/반/번호/학생이름/보호자서명)은 템플릿에 고정 포함됩니다.`,
        `- 회신란이 불필요한 경우, {{회신제목}}, {{회신안내}}, {{동의선택}}은 빈 문자열로 처리됩니다.`,
        ``,
        `## 예시`,
        ``,
        `> **제목**: 1학기 현장체험학습 안내`,
        `> **인사말**: 학부모님, 안녕하십니까? 푸른 신록이 아름다운 계절, 가정에 건강과 행복이 함께하시길 바랍니다.`,
        `> **본문**: 본교에서는 학생들의 체험 중심 학습을 위해 아래와 같이 현장체험학습을 실시하오니 안내드립니다.`,
      ].filter(Boolean).join('\n');

      return {
        content: [{ type: 'text' as const, text: guideMarkdown }],
      };
    },
  );
}
