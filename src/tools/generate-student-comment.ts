import { z } from 'zod';
import type { McpServer } from '@modelcontextprotocol/sdk/server/mcp.js';

export function registerStudentCommentTool(server: McpServer) {
  server.tool(
    'generate_student_comment',
    '생활기록부(생기부) 또는 성적표용 학생 코멘트를 생성하기 위한 가이드라인과 프롬프트를 반환합니다. 학생의 특성 키워드를 입력하면 NEIS 입력에 적합한 형식의 코멘트 작성 지침을 제공합니다.',
    {
      student_name: z.string().optional().describe('학생 이름 (선택, 익명 처리 가능)'),
      grade_level: z.string().describe('학년 (예: "초등 3학년", "중학교 2학년", "고등학교 1학년")'),
      subject: z.string().describe('과목명 (예: "국어", "수학", "사회", "과학")'),
      achievement_level: z.enum(['매우 잘함', '잘함', '보통', '노력 요함']).describe('성취 수준'),
      keywords: z.array(z.string()).describe('학생 특성 키워드 (예: ["발표 적극적", "글쓰기 창의적", "협동 우수", "독서 습관 좋음"])'),
      comment_type: z.enum(['교과세특', '행동특성', '독서활동', '창의적체험활동']).default('교과세특').describe('코멘트 유형'),
      max_chars: z.number().int().min(50).max(1000).default(500).describe('최대 글자 수 (NEIS 입력 제한에 맞춤)'),
      tone: z.enum(['격려형', '객관형', '발전형']).default('격려형').describe('어조 스타일'),
      include_growth: z.boolean().default(true).describe('성장 과정 포함 여부'),
    },
    async (params) => {
      const studentLabel = params.student_name || '해당 학생';

      const guideMarkdown = [
        `# 생활기록부 코멘트 생성 가이드`,
        ``,
        `## 입력 정보`,
        `| 항목 | 내용 |`,
        `|------|------|`,
        `| 학생 | ${studentLabel} |`,
        `| 학년 | ${params.grade_level} |`,
        `| 과목 | ${params.subject} |`,
        `| 성취 수준 | ${params.achievement_level} |`,
        `| 코멘트 유형 | ${params.comment_type} |`,
        `| 최대 글자 수 | ${params.max_chars}자 |`,
        `| 어조 | ${params.tone} |`,
        `| 성장 과정 포함 | ${params.include_growth ? '예' : '아니오'} |`,
        ``,
        `## 학생 특성 키워드`,
        params.keywords.map(k => `- ${k}`).join('\n'),
        ``,
        `## 작성 지침`,
        ``,
        `### 기본 원칙`,
        `1. **구체적 행동 중심**: 추상적 표현("착하다", "성실하다") 대신 구체적 행동과 사례를 기술합니다.`,
        `2. **과정 중심 서술**: 결과보다 학습 과정에서의 노력과 변화를 강조합니다.`,
        `3. **긍정적 표현**: 부정적 표현 대신 발전 가능성과 잠재력을 중심으로 서술합니다.`,
        `4. **개별화**: 해당 학생만의 고유한 특성이 드러나도록 작성합니다.`,
        `5. **글자 수 준수**: 반드시 ${params.max_chars}자 이내로 작성합니다.`,
        ``,
        `### 코멘트 유형별 작성법`,
        ``,
        getCommentTypeGuide(params.comment_type),
        ``,
        `### 성취 수준별 표현 가이드`,
        ``,
        getAchievementGuide(params.achievement_level),
        ``,
        `### 어조 스타일: ${params.tone}`,
        ``,
        getToneGuide(params.tone),
        ``,
        `### 금지 표현`,
        `- "~하지 못함", "부족함" 등 부정적 단정 표현`,
        `- 다른 학생과의 비교 표현`,
        `- "항상", "절대" 등 극단적 표현`,
        `- 성별 고정관념이 반영된 표현`,
        ``,
        `### 권장 문장 구조`,
        `\`\`\``,
        `[도입] 수업/활동에 대한 태도 + [전개] 구체적 활동 사례 + [마무리] 성장/발전 가능성`,
        `\`\`\``,
        ``,
        `## 출력 형식`,
        `- 하나의 문단으로 작성 (줄바꿈 없이)`,
        `- ${params.max_chars}자 이내`,
        `- 존댓말 사용하지 않음 (3인칭 서술체: "~함", "~임", "~됨")`,
        `- 마침표로 문장 종결`,
        ``,
        `## 예시 (참고용)`,
        ``,
        getExampleComment(params.comment_type, params.achievement_level),
      ].join('\n');

      return {
        content: [{ type: 'text' as const, text: guideMarkdown }],
      };
    },
  );
}

function getCommentTypeGuide(type: string): string {
  const guides: Record<string, string> = {
    '교과세특': [
      `#### 교과학습발달상황 세부능력 및 특기사항`,
      `- 해당 과목의 학습 목표 달성 과정을 서술합니다.`,
      `- 수업 중 보여준 구체적 활동(발표, 토론, 글쓰기, 실험 등)을 포함합니다.`,
      `- 성취기준과 연계된 역량(의사소통, 비판적 사고, 창의성 등)을 언급합니다.`,
      `- 구조: [과목 흥미/태도] + [구체적 활동 사례 2~3개] + [역량 발현] + [성장/발전]`,
    ].join('\n'),
    '행동특성': [
      `#### 행동특성 및 종합의견`,
      `- 학교생활 전반에서의 인성, 사회성, 리더십을 서술합니다.`,
      `- 교우관계, 봉사활동, 학교 행사 참여 등 구체적 사례를 포함합니다.`,
      `- 구조: [전반적 성격/태도] + [대인관계] + [특기할 활동] + [종합 평가]`,
    ].join('\n'),
    '독서활동': [
      `#### 독서활동상황`,
      `- 읽은 책의 제목과 저자를 명시합니다.`,
      `- 독서 후 보여준 사고의 깊이, 감상, 적용 사례를 서술합니다.`,
      `- 구조: [도서명(저자)] + [핵심 내용 이해] + [자신의 삶/학습에 적용한 점]`,
    ].join('\n'),
    '창의적체험활동': [
      `#### 창의적 체험활동`,
      `- 자율활동, 동아리활동, 봉사활동, 진로활동 중 해당 영역을 명시합니다.`,
      `- 활동 과정에서의 역할, 기여, 성장을 구체적으로 서술합니다.`,
      `- 구조: [활동명/영역] + [역할과 기여] + [배운 점/성장]`,
    ].join('\n'),
  };
  return guides[type] || guides['교과세특'];
}

