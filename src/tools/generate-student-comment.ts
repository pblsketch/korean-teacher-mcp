function getToneGuide(tone: string): string {
  const guides: Record<string, string> = {
    '격려형': `학생의 노력과 성장을 인정하고, 앞으로의 발전 가능성을 긍정적으로 서술합니다. "~하여 돋보임", "~하는 모습이 인상적임" 등의 표현을 활용합니다.`,
    '객관형': `관찰된 사실과 행동을 중심으로 객관적이고 담백하게 서술합니다. "~함", "~을 수행함", "~에 참여함" 등의 표현을 활용합니다.`,
    '발전형': `현재 수준을 인정하면서 구체적인 발전 방향을 제시합니다. "~하면 더욱 성장할 것으로 기대됨", "~을 통해 역량이 강화될 것임" 등의 표현을 활용합니다.`,
  };
  return guides[tone] || guides['격려형'];
}

function getExampleComment(type: string, level: string): string {
  if (type === '교과세특' && level === '매우 잘함') {
    return `> 문학 작품 감상에 깊은 흥미를 보이며, 소설 속 인물의 심리를 다각도로 분석하는 능력이 탁월함. '토론으로 읽는 문학' 활동에서 작품의 주제 의식을 사회적 맥락과 연결하여 발표하였으며, 모둠원들의 다양한 해석을 종합하여 새로운 관점을 도출하는 비판적 사고력을 발휘함. 자신의 생각을 논리적으로 구조화하여 글로 표현하는 역량이 지속적으로 성장하고 있음.`;
  }
  if (type === '교과세특' && level === '보통') {
    return `> 수업 활동에 성실히 참여하며 기본 학습 내용을 이해하려는 노력을 보임. 모둠 활동에서 자신의 역할을 수행하며 점차 자신감 있게 의견을 표현하는 모습이 관찰됨. 글쓰기 활동에서 자신의 경험을 솔직하게 표현하는 강점이 있으며, 다양한 글감을 접하면서 표현력이 향상되고 있음.`;
  }
  return `> (위 지침에 따라 ${type} 유형, ${level} 수준에 맞는 코멘트를 생성해주세요.)`;
}
