def zip_replace(src_path, dst_path, repls):
    """Read HWPX zip, apply text replacements to Contents/*.xml, write back."""
    tmp = dst_path + ".tmp"
    with zipfile.ZipFile(src_path, "r") as zin:
        with zipfile.ZipFile(tmp, "w", zipfile.ZIP_DEFLATED) as zout:
            for item in zin.infolist():
                data = zin.read(item.filename)
                if item.filename.startswith("Contents/") and item.filename.endswith(".xml"):
                    text = data.decode("utf-8")
                    for old, new in repls:
                        if old:
                            text = text.replace(old, new)
                    data = text.encode("utf-8")
                if item.filename == "mimetype":
                    zout.writestr(item, data, compress_type=zipfile.ZIP_STORED)
                else:
                    zout.writestr(item, data)
    if os.path.exists(dst_path):
        os.remove(dst_path)
    os.rename(tmp, dst_path)


def main():
    os.makedirs(os.path.dirname(OUTPUT), exist_ok=True)

    # Step 1: Build HWPX from base + pbl overlay
    with zipfile.ZipFile(OUTPUT, "w", zipfile.ZIP_DEFLATED) as zf:
        # mimetype first, STORED
        zf.write(os.path.join(BASE, "mimetype"), "mimetype", compress_type=zipfile.ZIP_STORED)

        # Collect base files
        base_files = {}
        for root, dirs, files in os.walk(BASE):
            for f in files:
                if f == "mimetype":
                    continue
                full = os.path.join(root, f)
                arcname = os.path.relpath(full, BASE).replace("\\", "/")
                base_files[arcname] = full

        # Overlay pbl files (override matching Contents/ entries)
        for f in os.listdir(PBL):
            full = os.path.join(PBL, f)
            if os.path.isfile(full) and f.endswith(".xml"):
                key = f"Contents/{f}"
                base_files[key] = full

        # Write all
        for arcname in sorted(base_files):
            zf.write(base_files[arcname], arcname)

    print(f"Built base HWPX: {OUTPUT}")

    # Step 2: ZIP-level text replacement
    zip_replace(OUTPUT, OUTPUT, replacements)
    print("Applied text replacements")

    # Step 3: Fix namespaces
    if os.path.exists(FIX_NS):
        subprocess.run(["python", FIX_NS, OUTPUT], check=True)
        print("Fixed namespaces")
    else:
        print(f"WARNING: fix_namespaces.py not found at {FIX_NS}, skipping")

    print("DONE")


if __name__ == "__main__":
    main()
